class Element {
  const Element({
    required this.name,
    required this.atomicNumber,
    required this.weight,
    required this.density,
    required this.meltingPoint,
    required this.boilingPoint,
    required this.heatCapacity,
    required this.electroNegativity,
    required this.abundance,
  });

  final String name;
  final double atomicNumber;
  final double weight;
  final double density;
  final double meltingPoint;
  final double boilingPoint;
  final double heatCapacity;
  final double electroNegativity;
  final double abundance;
}
